package estructurasBasicas;

public class VariableInstancia {
 private int engPoint;
 private int mathsPoints;
 
 
 
 
public VariableInstancia(int engPoint, int mathsPoints) {
	super();
	this.engPoint = engPoint;
	this.mathsPoints = mathsPoints;
}
public VariableInstancia() {
	super();
}
public int getEngPoint() {
	return engPoint;
}
public void setEngPoint(int engPoint) {
	this.engPoint = engPoint;
}
public int getMathsPoints() {
	return mathsPoints;
}
public void setMathsPoints(int mathsPoints) {
	this.mathsPoints = mathsPoints;
}
 
 
}
